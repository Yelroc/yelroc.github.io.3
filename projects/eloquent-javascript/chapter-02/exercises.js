// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for(let i = 1; i < 16; i++){
    if(i % 3 === 0 && i % 5 === 0){
      console.log('fizzbuzz');
    } else if(i % 3 === 0){
      console.log('fizz');
    } else if(i % 5 === 0){
      console.log('buzz');
    } else {
      console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  let board = '';
  for(let x = 0; x < num; x++){
    for(let y = 0; y < num; y++){
      board += (x % 2 === 0) === (y % 2 === 0) ? " " : "#";
    }
    board += '\n';
  }
  console.log(board);
}


