// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num){
  // if num has remainders
  if(num % 2 !== 0){
    // retunr false
    return false;
  } else {
    // return true
    return true;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char){
  // turn the string into an array of letters
  let str = string.split('');
  // set a count variable to keep count of the matches
  let count = 0;
  // loop through each letter
  str.forEach(letter => {
    // if the letter matches the charracter increment the count variable
    if(letter === char){
      count += 1;
    }
  });
  // return count
  return count;
}

