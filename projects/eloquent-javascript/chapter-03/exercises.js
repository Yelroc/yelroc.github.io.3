// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(first, second){
  // if first is bigger or equal to second
  if(first >= second){
    // return second
    return second;
    // if not return first
  } else {
    return first;
  }
}

